//************************************************************
//	親クラス [CBlock] のメンバ関数
//************************************************************
//============================================================
//	コンストラクタ
//============================================================
CBlock::CBlock(const EType type) :
	m_type		(type),			// 種類
	m_pos		(GRID2_ZERO),	// 原点位置
	m_fCurTime	(0.0f)			// 現在の待機時間
{

}

//============================================================
//	デストラクタ
//============================================================
CBlock::~CBlock()
{

}

//============================================================
//	初期化処理
//============================================================
HRESULT CBlock::Init(void)
{
	// メンバ変数を初期化
	m_pos = POSGRID2(5, 20);	// 原点位置
	m_fCurTime = 0.0f;			// 現在の待機時間

	// 成功を返す
	return S_OK;
}

//============================================================
//	終了処理
//============================================================
void CBlock::Uninit(void)
{

}

//============================================================
//	更新処理
//============================================================
void CBlock::Update(const float fDeltaTime)
{
	// 待機時間を加算
	m_fCurTime += fDeltaTime;
	if (m_fCurTime >= 3.0f)
	{
		// 原点位置に重力を与える
		m_pos.y--;
	}
}

//============================================================
//	生成処理
//============================================================
CBlock *CBlock::Create(const EType type)
{
	// ブロックの生成
	CBlock *pBlock = new CBlock(type);
	if (pBlock == nullptr)
	{ // 生成に失敗した場合

		return nullptr;
	}
	else
	{ // 生成に成功した場合

		// ブロックの初期化
		if (FAILED(pBlock->Init()))
		{ // 初期化に失敗した場合

			// ブロックの破棄
			SAFE_DELETE(pBlock);
			return nullptr;
		}

		// 確保したアドレスを返す
		return pBlock;
	}
}
